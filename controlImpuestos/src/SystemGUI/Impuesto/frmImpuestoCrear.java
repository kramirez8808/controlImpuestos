    private void jlCrearMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jlCrearMouseClicked
        //Obtener los datos de los campos
        descripcion = taDescripcion.getText();
        tasaImpuesto = tfTasa.getText();
        fechaVencimientno = tfFecha.getText();
    

        //Validar que los campos no esten vacios
        if (descripcion == "") {
            JOptionPane.showMessageDialog(null, "Ingrese la descripcion");
        } else if (tasaImpuesto == "" || tasaImpuesto.equals("Ingrese la tasa del impuesto")) {
            JOptionPane.showMessageDialog(null, "Ingrese la tasa del impuesto");
        } else if (fechaVencimientno == "" || fechaVencimientno.equals("Ingrese la fecha de vencimiento")) {
            JOptionPane.showMessageDialog(null, "Ingrese la fecha de vencimiento");
        } else {
            //Crear el objeto impuesto
            cImpuestos impuesto = new cImpuestos(descripcion, Double.parseDouble(tasaImpuesto), fechaVencimientno);

            //Agregar el los datos del objeto impuesto a la DB
            try {
                //Instanciar la clase crudImpuestos
                crudImpuestos crud = new crudImpuestos();

                //Llamar al metodo para guardar el impuesto en DB
                crud.guardarImpuesto(impuesto);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error al guardar el impuesto.");
            }


            //Limpiar los campos
            tfTasa.setText("Ingrese la tasa del impuesto");
            tfTasa.setForeground(new Color(153, 153, 153));
            tfFecha.setText("Ingrese la fecha de vencimiento");
            tfFecha.setForeground(new Color(153, 153, 153));
            taDescripcion.setText("");
        }

        

    }//GEN-LAST:event_jlCrearMouseClicked

